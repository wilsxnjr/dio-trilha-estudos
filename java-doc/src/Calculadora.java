public class Calculadora {

    /**
     * Este método é para somar dois números
     * @param numeroUm
     * @param numeroDois
     * @return o resultado deste método é a soma de dois números
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }

}
